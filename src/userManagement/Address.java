package userManagement;

class Address {
    String city;
    String street;
    String houseNumber;
    String postalCode;

    public Address(String city, String street, String houseNumber, String postalCode) {
        this.city = city;
        this.street = street;
        this.houseNumber = houseNumber;
        this.postalCode = postalCode;
    }
}

